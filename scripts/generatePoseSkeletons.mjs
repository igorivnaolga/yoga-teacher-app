const CATEGORY_FALLBACK = {
  standing: () => baseStanding(),
  seated: () => BY_ID['easy-seat'],
  supine: () => BY_ID.corpse,
  prone: () => BY_ID.locust,
  balance: () => BY_ID.tree,
  inversion: () => BY_ID['supported-shoulderstand'],
  twist: () => BY_ID['seated-twist'],
  forward_fold: () => BY_ID['forward-fold'],
  backbend: () => BY_ID.cobra,
  hip_opener: () => BY_ID.pigeon,
  arm_balance: () => BY_ID.plank,
  restorative: () => BY_ID.child,
};

function serializeJoints(j) {
  const keys = [
    'head',
    'neck',
    'lShoulder',
    'rShoulder',
    'lElbow',
    'rElbow',
    'lHand',
    'rHand',
    'pelvis',
    'lKnee',
    'rKnee',
    'lFoot',
    'rFoot',
  ];
  const body = keys.map((k) => `    ${k}: { x: ${j[k].x}, y: ${j[k].y} }`).join(',\n');
  return `{\n${body},\n  }`;
}

const missing = [];
const entries = [];
for (const pose of poses) {
  let joints = BY_ID[pose.id];
  if (!joints) {
    missing.push(pose.id);
    joints = CATEGORY_FALLBACK[pose.category]?.() ?? baseStanding();
  }
  entries.push(`  '${pose.id}': ${serializeJoints(joints)}`);
}

const out = `/* Auto-generated by scripts/generatePoseSkeletons.mjs — edit that script, then re-run. */
import type { StickJoints } from './types';

export const POSE_SKELETONS: Record<string, StickJoints> = {
${entries.join(',\n')},
};
`;

